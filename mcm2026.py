USE_PROXY = False
PROXIES = {
    "http": "http://your_proxy_here",
    "https": "http://your_proxy_here"
}

def fetch_trends(kw_list, timeframe):
    retry_count = 0

    while retry_count < MAX_RETRIES:
        try:
            pytrends = TrendReq(hl='en-US', tz=360, requests_args={"proxies": PROXIES} if USE_PROXY else {})
            pytrends.build_payload(kw_list, cat=0, timeframe=timeframe, geo='', gprop='')
            df = pytrends.interest_over_time()
            if not df.empty:
                df = df.drop(columns=['isPartial'], errors='ignore')
            return df
        except Exception as e:
            print(f"[Retry {retry_count+1}] 网络错误：{e}")
            retry_count += 1
            time.sleep(REQUEST_DELAY * retry_count)  # 指数型回退

    return pd.DataFrame()


