import pymc as pm
import arviz as az

def wk1_fanvote_model(r_J, alpha=2.0, beta=1.0, w=0.5):
    N = len(r_J)
    with pm.Model() as model:
      x = pm.Dirichlet("x", a=np.ones(N) * alpha)
      # 危险度 z 可照常构造
      z = w * r_J - (1 - w) * x * N
      p_elim = pm.math.softmax(beta * z)
      # ❌ 不提供 eliminated_idx → 模型只采样 x，没有观测约束

      return model

def bayesian_fanvote_model(r_J, eliminated_idx, alpha=2.0, beta=1.0, w=0.5):
    N = len(r_J)  # 本轮选手数

    with pm.Model() as model:
        # Dirichlet prior over audience shares
        x = pm.Dirichlet("x", a=np.ones(N) * alpha)

        # 构造“危险度”score（连续形式）
        # r_J 越大越危险
        # x 越小越危险 → 用 -x
        z = w * r_J - (1 - w) * x * N   # 乘 N 让量级接近

        # Softmax likelihood
        p_elim = pm.math.softmax(beta * z)

        # Observed elimination
        elim = pm.Categorical("elim", p=p_elim, observed=eliminated_idx)

    return model

def final_fanvote_model(r_J, placement, alpha=2.0, beta=1.0, w=0.5):
  N = len(r_J)  # 本轮选手数

  ranked_indices = placement.argsort()

  with pm.Model() as model:
        x = pm.Dirichlet("x", a=np.ones(N) * alpha)
        z = -x * N  # 投票多者排前面
        p_rank = pm.math.softmax(beta * z)

        # 多个 soft 观察：rank1, rank2, rank3...
        for i, idx in enumerate(ranked_indices):
            pm.Categorical(f"rank_{i+1}", p=p_rank, observed=idx)

  return model

