def solve_final_week_lp(df_week):
    df_week = df_week.reset_index(drop=True)
    n = len(df_week)
    y = df_week["score_percent"].values

    sorted_indices = df_week.sort_values("placement").index.tolist()

    c_dummy = np.zeros(n)
    A_eq = [np.ones(n)]
    b_eq = [1]
    A_ub = []
    b_ub = []

    # 根据排名构造顺序约束
    delta = 1e-6
    for i in range(n):
        for j in range(i + 1, n):
            i_better = sorted_indices[i]
            i_worse = sorted_indices[j]

            constraint = np.zeros(n)
            constraint[i_worse] = 1   # x_worse
            constraint[i_better] = -1 # -x_better
            rhs = y[i_better] - y[i_worse] - delta # score_better - score_worse

            A_ub.append(constraint)
            b_ub.append(rhs)

    bounds = [(0, None)] * n

    res = linprog(c=c_dummy, A_eq=A_eq, b_eq=b_eq,
                  A_ub=A_ub, b_ub=b_ub, bounds=bounds, method="highs")

    if not res.success:
        print("规划失败")
        return None

    # audience range for each player
    audience_min = []
    audience_max = []

    for i in range(n):
        # min
        c_min = np.zeros(n)
        c_min[i] = 1
        res_min = linprog(c=c_min, A_eq=A_eq, b_eq=b_eq,
                          A_ub=A_ub, b_ub=b_ub, bounds=bounds, method="highs")
        # max
        c_max = np.zeros(n)
        c_max[i] = -1
        res_max = linprog(c=c_max, A_eq=A_eq, b_eq=b_eq,
                          A_ub=A_ub, b_ub=b_ub, bounds=bounds, method="highs")

        if res_min.success and res_max.success:
            audience_min.append(res_min.fun)
            audience_max.append(-res_max.fun)
        else:
            audience_min.append(None)
