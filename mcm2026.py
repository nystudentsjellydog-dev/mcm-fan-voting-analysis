def solve_week_lp(df_week):

    df_week = df_week.reset_index(drop=True)
    n = len(df_week)
    elim_idx = df_week[df_week["eliminated"]].index[0]  # 被淘汰者的行索引
    y = df_week["score_percent"].values                 # y_i 是裁判得分百分比

    # linprog setup
    c = np.zeros(n)  # 无目标函数，仅为可行解
    A_eq = [np.ones(n)]
    b_eq = [1]

    A_ub = []
    b_ub = []

    delta = 1e-6
    for j in range(n):
        if j == elim_idx:
            continue
        constraint = np.zeros(n)
        constraint[elim_idx] = 1   # x_elim
        constraint[j] = -1         # -x_j
        rhs = y[j] - y[elim_idx] - delta   # y_j - y_elim

        A_ub.append(constraint)
        b_ub.append(rhs)

    bounds = [(0, None) for _ in range(n)]  # x_i ≥ 0

    res = linprog(c=c, A_eq=A_eq, b_eq=b_eq, A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')

    if not res.success:
        print("线性规划失败！")
        return None

    audience_min = []
    audience_max = []

    for i in range(n):
      # 求最小 x[i]
      c_min = np.zeros(n)
      c_min[i] = 1
      res_min = linprog(c=c_min, A_eq=A_eq, b_eq=b_eq,
                          A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')

        # 求最大 x[i]
      c_max = np.zeros(n)
      c_max[i] = -1
      res_max = linprog(c=c_max, A_eq=A_eq, b_eq=b_eq,
                          A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')

      if res_min.success and res_max.success:
          audience_min.append(res_min.fun)
          audience_max.append(-res_max.fun)  # 注意取负

      else:
          audience_min.append(None)
          audience_max.append(None)

    # ===== 构造结果 =====
    df_week = df_week.copy()
    df_week["audience_percent"] = res.x
    df_week["audience_min"] = audience_min
    df_week["audience_max"] = audience_max
    df_week["combined_score"] = df_week["score_percent"] + df_week["audience_percent"]
    df_week["combined_min"] = df_week["score_percent"] + df_week["audience_min"]
    df_week["combined_max"] = df_week["score_percent"] + df_week["audience_max"]

    return df_week


