    df_week = df_week.copy()
    df_week["audience_percent"] = res.x
    df_week["audience_min"] = audience_min
    df_week["audience_max"] = audience_max
    df_week["combined_score"] = df_week["score_percent"] + df_week["audience_percent"]
    df_week["combined_min"] = df_week["score_percent"] + df_week["audience_min"]
    df_week["combined_max"] = df_week["score_percent"] + df_week["audience_max"]

